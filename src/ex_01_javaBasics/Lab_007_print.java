package ex_01_javaBasics;

public class Lab_007_print {

    // Class name (PrintExample) is an identifier
        public static void main(String[] args) {
            // Using print to display text without moving to a new line
            System.out.print("Hello, ");
            System.out.print("World! ");

            // Output: Hello, World!

            // Printing numbers
            System.out.print("The sum of 5 and 3 is: ");
            System.out.print(5 + 3);

            // Output: The sum of 5 and 3 is: 8

            // Printing without a new line
            System.out.print(" Java is awesome!");

            // Output: Java is awesome!
        }
    }


