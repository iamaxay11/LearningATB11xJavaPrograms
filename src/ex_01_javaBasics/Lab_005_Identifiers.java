package ex_01_javaBasics;

// Class name 'IdentifierExample' is an identifier

public class Lab_005_Identifiers {

    //Identifiers are names given to variables, methods, classes, etc. They must start with a letter, $, or _ and cannot be a keyword.

    // Method name 'main' is an identifier
    public static void main(String[] args) {

        // Variable identifiers
        String studentName = "Alice"; // 'studentName' is an identifier
        int studentAge = 20;  // 'studentAge' is an identifier
        double totalMarks = 85.5; // 'totalMarks' is an identifier

        // Constants (Identifiers in uppercase by convention)
        final double PI = 3.14159; // 'PI' is an identifier (constant)

        System.out.println(studentName);

    }

}
