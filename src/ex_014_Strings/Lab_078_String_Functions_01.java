package ex_014_Strings;
// toUpperCase() , toLowerCase() , length()
public class Lab_078_String_Functions_01 {
    public static void main(String[] args) {
        String str1 = "Itsakshay10";

        // toUpperCase()
        System.out.println(str1.toUpperCase()); // ITSAKSHAY10

        // toLowerCase()
        System.out.println(str1.toLowerCase()); // itsakshay10

        // length()
        System.out.println(str1.length()); // 11
    }
}
