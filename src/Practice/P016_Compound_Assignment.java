package Practice;

// Demonstrate compound assignment operators (+=, -=, *=, /=, %=, &=, |=, ^=).

public class P016_Compound_Assignment {
    public static void main(String[] args) {

        int a = 10;
        int b = 25;

        System.out.println(a+=5);
        System.out.println(a-=5);
        System.out.println(a*=5);
        System.out.println(a/=5);
        System.out.println(a%=5);

    }
}
