package Practice;

// Declare all primitive data types in Java, assign values, and print them.

public class P003_dataTypes {
    public static void main(String[] args) {
        byte b = 72;
        short s = 8765;
        int i = 476548765;
        long l = 9876547880987L;
        float f = 13.876543f;
        double d = 656.827365467892837465d;
        char c = 'A';
        boolean bool = true;

        System.out.println(b);
        System.out.println(s);
        System.out.println(i);
        System.out.println(l);
        System.out.println(f);
        System.out.println(d);
        System.out.println(c);
        System.out.println(bool);
    }
}
