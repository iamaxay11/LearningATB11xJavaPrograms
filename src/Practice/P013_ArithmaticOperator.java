package Practice;

// Perform all arithmetic operations (+, -, *, /, %).

public class P013_ArithmaticOperator {
    public static void main(String[] args) {

        int a = 10;
        int b = 4;

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);

    }
}
