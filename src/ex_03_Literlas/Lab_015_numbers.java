package ex_03_Literlas;

public class Lab_015_numbers {
    public static void main(String[] args) {
        int age = 30;        // Integer literal
        long distance = 5000000000L; // Long literal (L suffix)
        float pi = 3.1415f;  // Float literal (f suffix)
        double gravity = 9.81; // Double literal (default)

        System.out.println("Integer Literal (age): " + age);
        System.out.println("Long Literal (distance): " + distance);
        System.out.println("Float Literal (pi): " + pi);
        System.out.println("Double Literal (gravity): " + gravity);
    }
}

